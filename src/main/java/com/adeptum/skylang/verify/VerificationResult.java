package com.adeptum.skylang.verify;

/** Outcome of verifying a staged project: whether it passed, plus captured toolchain output. */
public record VerificationResult(boolean passed, String output) {

    public static VerificationResult pass() {
        return new VerificationResult(true, "");
    }

    public static VerificationResult fail(String output) {
        return new VerificationResult(false, output);
    }
}
